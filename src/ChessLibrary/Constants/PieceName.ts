/**
 * Enumeration representing the names of the chess pieces.
 * @packageDocumentation
 */

/**
 * @enum
 * @public
 *
 * @property {number} Pawn - Represents a pawn piece.
 * @property {number} Rook - Represents a rook piece.
 * @property {number} Knight - Represents a knight piece.
 * @property {number} Bishop - Represents a bishop piece.
 * @property {number} Queen - Represents a queen piece.
 * @property {number} King - Represents a king piece.
 */
export const enum PieceName {
    Pawn,
    Rook,
    Knight,
    Bishop,
    Queen,
    King
}